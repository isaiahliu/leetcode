    class Solution {
        fun repairCars(ranks: IntArray, cars: Int): Long {
            var min = ranks.min().toLong()
            var max = ranks.max().toLong() * cars * cars

            var result = 0L
            while (min <= max) {
                val mid = (min + max) / 2

                var repairCars = 0L
                ranks.forEach {
                    repairCars += sqrt(mid.toDouble() / it).toLong()
                }

                if (repairCars >= cars) {
                    result = mid
                    max = mid - 1
                } else {
                    min = mid + 1
                }
            }

            return result
        }
    }

