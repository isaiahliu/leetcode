    class Solution {
        fun maximumScore(nums: IntArray, k: Int): Int {
            val queue =
                PriorityQueue(compareByDescending<Pair<Int, Int>> { it.first }.thenBy { if (it.second > k) it.second - k else k - it.second })
            arrayOf(-1, 1).forEach { delta ->
                var t = k + delta
                var min = nums[k]

                while (true) {
                    if (t in nums.indices) {
                        nums[t].takeIf { it < min }?.also {
                            queue.add(min to t - delta)
                            min = it
                        }
                        t += delta
                    } else {
                        queue.add(min to t - delta)
                        break
                    }
                }
            }

            var leftMin = nums[k]
            var rightMin = nums[k]
            var leftIndex = k
            var rightIndex = k
            var result = nums[k]

            while (queue.isNotEmpty()) {
                val (m, index) = queue.poll()

                if (index > k) {
                    rightIndex = index
                    rightMin = m
                } else {
                    leftIndex = index
                    leftMin = m
                }
                result = result.coerceAtLeast(leftMin.coerceAtMost(rightMin) * (rightIndex - leftIndex + 1))
            }

            return result
        }
    }

