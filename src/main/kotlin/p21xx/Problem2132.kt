    class Solution {
        fun possibleToStamp(grid: Array<IntArray>, stampHeight: Int, stampWidth: Int): Boolean {
            class SegNode(var from: Int, var to: Int) {
                var value = false

                val children = arrayOfNulls<SegNode>(2)

                fun mark(s: Int, e: Int) {
                    when {
                        value -> {}
                        s > to || e < from -> {}
                        s <= from && e >= to -> {
                            value = true
                        }

                        else -> {
                            if (children[0] == null) {
                                val mid = (from + to) / 2

                                children[0] = SegNode(from, mid)
                                children[1] = SegNode(mid + 1, to)
                            }

                            children.forEach {
                                it?.mark(s, e)
                            }

                            if (children.none { it?.value == false }) {
                                value = true
                            }
                        }
                    }
                }
            }

            val sums = Array(grid.size) { r ->
                IntArray(grid[0].size)
            }

            val segs = Array(grid.size) {
                SegNode(0, grid[it].lastIndex)
            }

            grid.forEachIndexed { r, row ->
                for (c in row.lastIndex downTo 0) {
                    if (row[c] == 0) {
                        sums[r][c] = (sums[r].getOrNull(c + 1) ?: 0) + 1
                    } else {
                        segs[r].mark(c, c)
                    }
                }
            }

            if (stampHeight <= grid.size && stampWidth <= grid[0].size) {
                for (c in 0 until grid[0].size - stampWidth + 1) {
                    var markIndex = 0
                    val list = LinkedList<Int>()

                    var matchSize = 0
                    repeat(stampHeight) { row ->
                        list.add(sums[row][c].also {
                            if (it >= stampWidth) {
                                matchSize++
                            } else {
                                markIndex = row + 1
                            }
                        })
                    }
                    var nextIndex = stampHeight

                    if (matchSize == stampHeight) {
                        while (markIndex < nextIndex) {
                            segs[markIndex++].mark(c, c + stampWidth - 1)
                        }
                    }

                    while (nextIndex < grid.size) {
                        if (list.poll() >= stampWidth) {
                            matchSize--
                        }

                        list.add(sums[nextIndex][c].also {
                            if (it >= stampWidth) {
                                matchSize++
                            } else {
                                markIndex = nextIndex + 1
                            }
                        })

                        nextIndex++

                        if (matchSize == stampHeight) {
                            while (markIndex < nextIndex) {
                                segs[markIndex++].mark(c, c + stampWidth - 1)
                            }
                        }
                    }
                }
            }

            return segs.all { it.value }
        }
    }
